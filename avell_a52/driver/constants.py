from enum import Enum


class Colors(Enum):
    aqua = [0x00, 0xFF, 0xFF]
    blue = [0x00, 0x00, 0xFF]
    fuchsia = [0xFF, 0x00, 0xFF]
    green = [0x00, 0xFF, 0x00]
    gray = [0x80, 0x80, 0x80]
    lime = [0x00, 0x80, 0x00]
    maroon = [0x80, 0x00, 0x00]
    navy = [0x00, 0x00, 0x80]
    olive = [0x80, 0x80, 0x00]
    purple = [0x80, 0x00, 0x80]
    red = [0xff, 0x00, 0x00]
    silver = [0xc0, 0xc0, 0xc0]
    teal = [0x00, 0x80, 0x80]
    white = [0xff, 0xff, 0xff]
    yellow = [0xff, 0xff, 0x00]
    orange = [0xff, 0x80, 0x00]


class Brightness(Enum):
    b0 = 0x00
    b1 = 0x08
    b2 = 0x16
    b3 = 0x24
    b4 = 0x32


class Directions(Enum):
    left2right = 0x01
    right2left = 0x02
    sync = 0x03


class Speed(Enum):
    s1 = 0x07
    s2 = 0x05
    s3 = 0x03
    s4 = 0x01
